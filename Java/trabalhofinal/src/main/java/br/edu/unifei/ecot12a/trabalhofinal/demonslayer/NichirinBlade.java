package br.edu.unifei.ecot12a.trabalhofinal.demonslayer;

public class NichirinBlade {
    private String bladeColor;
    private String attribute;
    private String ore;
    public String getBladeColor() {
        return bladeColor;
    }
    public void setBladeColor(String bladeColor) {
        this.bladeColor = bladeColor;
    }
    public String getAttribute() {
        return attribute;
    }
    public void setAttribute(String attribute) {
        this.attribute = attribute;
    }
    public String getOre() {
        return ore;
    }
    public void setOre(String ore) {
        this.ore = ore;
    }
    
}
